    private Usuarios viewBean() {
        Usuarios ebmUsuario = new Usuarios();
        int codigo = Integer.valueOf(jTxtCodigo.getText());
        ebmUsuario.setIdusuarios(codigo);
        ebmUsuario.setNome(jTxtNome.getText());
        ebmUsuario.setApelido(jTxtApelido.getText());
        ebmUsuario.setCpf(jFmtCpf.getText());
        try {
            ebmUsuario.setDataNascimento(dtnFormat.parse(jFmtData.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(JDlgUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        ebmUsuario.setSenha(jPwfSenha.getText());
        ebmUsuario.setNivel(jCboNivel.getSelectedIndex());
        if (jChbAtivo.isSelected() == true) {
            ebmUsuario.setAtivo("S");
        } else {
            ebmUsuario.setAtivo("N");
        }
        return ebmUsuario;
    }

    protected void beanView(Object object) {
        Usuarios ebmUsuarios = (Usuarios) object;
        if (ebmUsuarios != null) {

            String cad1 = String.valueOf(ebmUsuarios.getIdusuarios());
            jTxtCodigo.setText(cad1);
            jTxtNome.setText(ebmUsuarios.getNome());
            jTxtApelido.setText(ebmUsuarios.getApelido());
            jFmtCpf.setText(ebmUsuarios.getCpf());
            jFmtData.setText(dtnFormat.format(ebmUsuarios.getDataNascimento()));
            jPwfSenha.setText(ebmUsuarios.getSenha());
            int num = Integer.valueOf(ebmUsuarios.getNivel());
            jCboNivel.setSelectedIndex(num);
            if (ebmUsuarios.getAtivo().equals("S")) {
                jChbAtivo.setSelected(true);
            } else {
                jChbAtivo.setSelected(false);
            }
        }
    }
    
